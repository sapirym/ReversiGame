package mazeapp;
/**
 * point class
 */
public class Point {
    private  int x;
    private int y;
    
    /**
     * constructor
     * @param x1 - the x point
     * @param y1 - the y point
     */
    public Point(int x1,int y1) {
        x=x1;
        y=y1;
    }
    /**
     * get the x value
     * @return x value
     */
    public int getX() {
        return x;
    }
    /**
     * get the y value
     * @return y value
     */
    public int getY() {
        return y;
    }
}
